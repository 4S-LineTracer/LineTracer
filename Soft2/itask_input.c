#pragma interrupt itask_input
void itask_input(void){
    // スイッチの値を更新
    SW_DATA = swInput();

    // センサの値を更新
    SENS_DATA = sensInput(SENS_PROOF_TIME);

    TSR3 &= ~0x01;
}
